public class Pessoa {

    private String nome;
    private String ende;

    public Pessoa() {}

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getNome() {
        return nome;
    }

    public void setEndereco(String ende) {
        this.ende = ende;
    }

    public String getEndereco() {
        return ende;
    }
}
